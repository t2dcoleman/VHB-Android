package com.t2.vhb;

import android.os.Build;
import android.os.StrictMode;
import android.support.multidex.MultiDexApplication;

import timber.log.Timber;

/**
 * @author wes
 */
public class VhbApplication extends MultiDexApplication {
    
    @Override
    public void onCreate() {
        super.onCreate();

        //TODO: This disables the fileuri access on android 23 and up, this is not ideal but in order
        //to not split the code between android n or above or below android n we must do this until our minimum is
        //android 23, then we will have to impliment the FileProvider solution
        if(Build.VERSION.SDK_INT >= Build.VERSION_CODES.N){
            StrictMode.VmPolicy.Builder builder = new StrictMode.VmPolicy.Builder();
            StrictMode.setVmPolicy(builder.build());
        }

        if (BuildConfig.DEBUG) {
            Timber.plant(new Timber.DebugTree());
        }

    }
}
